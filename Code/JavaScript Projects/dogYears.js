//Variable: My age
let myAge = 17;

//Variable: Early dog years
let earlyYears = 2;
earlyYears = earlyYears * 10.5;

//Variable: Later dog years
let laterYears = myAge - 2;

//Multiply later years by 4
laterYears *= 4;

//Variable: adds the early and later years together
let myAgeInDogYears = earlyYears + laterYears;

//Variable: My name
const myName = 'Landon'.toLowerCase();

//log stating my name, age, and dog age
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);